package client.model;


public class Board {
	private int bno;
	private String btitle;
	private String bcontent;
	private int bhitcount;
	private String bwriter;
	private String bwritedate;
	private String barea;
	private String bappointtime;
	private String bappointday;
	private int bcommentcnt;
	
	public int getBno() {
		return bno;
	}
	public void setBno(int bno) {
		this.bno = bno;
	}
	public String getBtitle() {
		return btitle;
	}
	public void setBtitle(String btitle) {
		this.btitle = btitle;
	}
	public String getBcontent() {
		return bcontent;
	}
	public void setBcontent(String bcontent) {
		this.bcontent = bcontent;
	}
	public int getBhitcount() {
		return bhitcount;
	}
	public void setBhitcount(int bhitcount) {
		this.bhitcount = bhitcount;
	}
	public String getBwriter() {
		return bwriter;
	}
	public void setBwriter(String bwriter) {
		this.bwriter = bwriter;
	}
	public String getBwritedate() {
		return bwritedate;
	}
	public void setBwritedate(String bwritedate) {
		this.bwritedate = bwritedate;
	}
	public String getBarea() {
		return barea;
	}
	public void setBarea(String barea) {
		this.barea = barea;
	}
	public String getBappointtime() {
		return bappointtime;
	}
	public void setBappointtime(String bappointtime) {
		this.bappointtime = bappointtime;
	}
	public String getBappointday() {
		return bappointday;
	}
	public void setBappointday(String bappointday) {
		this.bappointday = bappointday;
	}
	public int getBcommentcnt() {
		return bcommentcnt;
	}
	public void setBcommentcnt(int bcommentcnt) {
		this.bcommentcnt = bcommentcnt;
	}


}
